     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $data = [];
        if (\Auth::check()) {
            $user = User::find($id);
            
             
         $groups = $user->groups()->paginate(10); 
          $profile=$user->profile()->getResults();
          $group = new Group;
         $data = [ 
             'user' => $user, 
             'groups' => $groups,
             'profile' =>$profile, 
             'group' => $group,
              
         ]; 
  
         $data += $this->counts($user); 
         return view('users.join', $data); 
        }else {
            return view('welcome');
        }
    }

